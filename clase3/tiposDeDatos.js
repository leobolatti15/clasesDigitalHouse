// Tipo de dato number

const edad = 30;
console.log(edad);

const precio = 150.65;
console.log(precio);

// Tipo de dato NaN - tipo de dato que no es numero pero tampoco string o booleano

const tuVieja = 'a' * 2;
console.log(tuVieja);

// Tipo de dato Null - para datos desconocidos 

const nulo = null;
console.log(nulo);

// Tipo de dato Undefined - cuando no se le asigna valor a la variable - no se puede declarar con Const papa

var sinValor;
console.log(sinValor);

sinValor = true;
console.log(sinValor);

// Tipo de dato objeto literarl

const persona = {
    nombre: 'Leonardo',
    apellido: 'Bolatti',
    edad: 30
};

console.log(persona);

// Tipo de dato array

const paises = ['Argentina', 'Venezuela', 'Paraguay'];

console.log(paises);